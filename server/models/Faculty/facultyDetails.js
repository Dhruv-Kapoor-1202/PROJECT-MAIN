import mongoose from "mongoose";

const facultyDetailsSchema = new mongoose.Schema({
  employeeID: {
    type: Number,
    required: true
  },
  fname: {
    type: String,
    required: true
  },
  lname: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  phoneNumber: {
    type: Number,
    required: true,
  },
  department: {
    type: String,
    required: true,
  },
  gender: {
    type: String,
    required: true,
  }
});

export default mongoose.model("facultyDetails", facultyDetailsSchema);